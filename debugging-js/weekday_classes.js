/*
The function toString had errors in converting a given date to its proper string
format. The .getYear() method was used when converting the date's year to a 
string. It is a deprecated method that returns an incorrect value for years 
2000 and above. Instead, .getFullYear() should be used. The .getMonth() method
returns the month from a 0 to 11 index. The (return value + 1) should be 
returned instead. The .getDay() method returns the day of the week, from indices
0 - 6. The method .getDate() should be used instead to get the day of the month. 
Finally, both the .getMonth() and .getDate() methods require a front padding of 
0s in case of a single digit number. To handle this, the .padStart() method is 
used. 
*/

function toDate(string) {
  return new Date(string + "T00:00:00");
}

const TODAY = toDate("2018-08-05");

function toString(date) {
  let year = date.getFullYear();
  let month = String(date.getMonth() + 1).padStart(2, "0");
  let day = String(date.getDate()).padStart(2,"0");
  return `${year}-${month}-${day}`;
}

function isInThePast(date) {
  return date < TODAY;
}

function isWeekday(date) {
  return date.getDay() >= 1 && date.getDay() <= 5;
}

